def add_function(num1, num2):
    result = str(int(num1) + int(num2))
    return result


def subtract_function(num1, num2):
    result = str(int(num1) - int(num2))
    return result


def multiply_function(num1, num2):
    result = str(int(num1) * int(num2))
    return result


def division_function(num1, num2):
    result = str(int(num1) // int(num2))
    return result
